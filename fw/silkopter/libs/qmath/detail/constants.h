namespace math
{
	//! Constant for PI.
	static const float kPI  = 3.14159265359f;

	//! Constant for half of PI.
	static const float kPI2 = kPI * 0.5f;

	//! Constant for two PI.
	static const float k2PI = kPI * 2.0f;

	//! Constant for 3 * PI / 2.
	static const float k3PI2 = 3.0f * kPI2;
}
